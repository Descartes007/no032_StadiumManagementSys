        return
      }
      if( 'yonghu' ==this.flag && this.ruleForm.youxiang&&(!isEmail(this.ruleForm.youxiang))){
        this.$message.error(`邮箱应输入邮箱格式`);
        return
      }
      if((!this.ruleForm.huiyuanzhanghao)&& 'huiyuan'==this.flag){
        this.$message.error('会员账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'huiyuan'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'huiyuan' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入手机格式`);
        return
      }
      if((!this.ruleForm.jiaoliangonghao)&& 'jianshenjiaolian'==this.flag){
        this.$message.error('教练工号不能为空');
        return
      }
      if((!this.ruleForm.jiaolianxingming)&& 'jianshenjiaolian'==this.flag){
        this.$message.error('教练姓名不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'jianshenjiaolian'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
